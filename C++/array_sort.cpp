class Solution
{
    public:
    void sort012(int a[], int n)
    {
        // code here 
        int tmp;
         for(int i=0;i<n;i++){
            cout << a[i]  << " ";
        }
        for(int i=0;i<n;i++)
        {
            for(int j=i+1;j<n;j++)
            {
                if(a[i]>a[j])
                {
                    tmp=a[i];
                    a[i]=a[j];
                    a[j]=tmp;
                }
            }
        }
        for(int i=0;i<n;i++){
            cout << a[i]  << " ";
        }
    }
    

    
};

